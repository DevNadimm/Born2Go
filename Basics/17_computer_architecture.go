/*
	# Computer Architecture & History

	--------------------------------
	Computer Architecture:
	--------------------------------
	Main Parts of a Computer:
	1. CPU -> Central Processing Unit
	2. RAM -> Random Access Memory
	3. ROM -> Read Only Memory

	CPU:
	- Known as the "brain" of the computer.
	- Performs calculations and logical operations such as (+, -, *, /, &&, ||, !).
	- Contains important registers such as the *Program Counter* and *Instruction Pointer*
	  which help in CPU processing.

	RAM:
	- Primary memory.
	- Volatile: data is lost when the power is turned off.
	- Used for temporary data storage and fast access.

	ROM:
	- Secondary or permanent memory.
	- Non-volatile: data remains even after power is turned off.
	- Used to store firmware and essential startup programs.

	--------------------------------
	Computer History:
	--------------------------------
	- Around 2700 BC: The world's first calculating device — *Abacus* — was invented.
	- 1600s: *Gottfried Wilhelm Leibniz* introduced the concept of *binary logic* (0 and 1 system).
	- 1800s: *Charles Babbage*, known as the "Father of the Computer," invented the 
	  *Analytical Engine*, a mechanical computer using punch cards for input and 
	  a printer for output.
	- *Ada Lovelace* is recognized as the first computer programmer for her work 
	  on the Analytical Engine.
	- 1930s: *Alan Turing* developed the concept of the *Turing Machine*, which became 
	  the foundation of modern computing.

	--------------------------------
	Generations of Computers:
	--------------------------------
	1️⃣ **First Generation (1940–1956)**  
	   - Used *vacuum tubes* for circuitry.  
	   - Example: *ENIAC* — the first electronic general-purpose computer, 
	     built for the U.S. Military.

	2️⃣ **Second Generation (1956–1963)**  
	   - Used *transistors* instead of vacuum tubes.  
	   - Smaller, faster, more reliable.

	3️⃣ **Third Generation (1964–1971)**  
	   - Introduced *Integrated Circuits (ICs)*.  
	   - Marked by the start of commercial computing (IBM, etc.).

	4️⃣ **Fourth Generation (1971–Present)**  
	   - Use of *microprocessors*.  
	   - Example: *Intel 4004* — the first 4-bit microprocessor (1971).  
	   - Rise of companies like *Intel, Apple, IBM, Microsoft*.  
	   - Introduction of *Personal Computers (PCs)* and *software industry*.

	5️⃣ **Fifth Generation (Present & Future)**  
	   - Based on *Artificial Intelligence (AI)* and *Quantum Computing*.  
	   - Uses advanced processors and quantum mechanics for ultra-fast computation.

	--------------------------------
	Modern Computing:
	--------------------------------
	- Most modern devices use *multi-core CPUs*, *GPUs*, and *cloud-based systems*.
	- *Internet* and *software* ecosystems (like Windows, macOS, Linux) drive today's computing.
	- *Quantum computers* aim to solve complex problems much faster than classical systems.
*/
